public class While_loop {
    public static void main(String[] args) {
        int i = 1; // initialization
        while (i <= 5) { // condition
            System.out.println(i); // statement
            i++; // increment/decrement(upadiing the condition)
        }
        int count = 500;
        while(count >= 200){
            System.out.println(count);
            count--; //unary operator
        }
    }
}