public class Expressions {

    public static void main(String[] args){
        //expression = operands & operators
        // operands = values, variables, numbers, quantity 
        //operators = + - * / %

        int friends = 10;
        friends = friends + 1;
        System.out.println("friends: "+friends);

        double point = 25;
        point = point / 2;
        System.out.println("point: "+point);
        
    }
}