public class typeConversion {

    public static void main(String[] args){
        double myFloat = 5.6f; // Implicit conversion
        System.out.println(myFloat);
        // Implicit Conversion is a method of converting one data type to another data type without the user having to do anything
        // ekhane boro data type theke choto data type a conversuion hoi automatically
        // ekhane bracket a data-type likhte hoina karan boro container a choto container assign kora jai

        int myInt = (int)5.45f; // Explicit conversion
        System.out.println(myInt);
        // Explicit Conversion is a method of converting one data type to another data type by the user
        // ekhane choto data type theke boro data type a conversion hoi user er kaj theke
    }
    
}
